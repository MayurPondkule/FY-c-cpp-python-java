#include<stdio.h>
void main(){
    // *************USE OF FOR LOOP**********
    //              FOR LOOP
     int i=0;
     for(int i=0;i<=4;i++){
         printf("value of i is %d\n", i);
     }

// *********USE OF WHILE LOOP*************
//            WHILE LOOP
    while(i<=5){
            printf("%d\n", i);
            i++;
        }



// **********USE OF DO WHILE**************

    do{
        printf("%d\n", i);
        i++;
    }
    while(i<=8);

     
}